
def find_day(index):
    if index==0:
        return "월"
    elif index==1:
        return "화"
    elif index==2:
        return "수"
    elif index==3:
        return "목"
    elif index==4:
        return "금"
    elif index==5:
        return "토"
    elif index==6:
        return "일"